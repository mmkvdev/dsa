// Implemeting Queues using Arrays

// Insertions and Removal

// push() and shift() for enqueue and dequeue operations

let a = [];
a.push("first");
a.push("second");
a.push("third");
a.shift();

// unshift() and pop() for enqueue and dequeue operations

let b = [];
b.unshift("first");
b.unshift("second");
b.unshift("third");
b.pop();
